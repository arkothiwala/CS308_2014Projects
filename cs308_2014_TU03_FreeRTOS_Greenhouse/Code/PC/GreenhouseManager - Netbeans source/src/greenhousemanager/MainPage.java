    public boolean isInteger(String str[], boolean allowNull)
    {
        try
        {
            int i;
            for(String s : str)
            {
                if((s == null || s.length() == 0) && allowNull)
                {
                    continue;
                }
                i = Integer.parseInt(s);
            }
            return true;
        } catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(this, "Give an Integer value as input.", "Not an Integer!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
    

    public boolean createPropertiesFile(String data[], boolean allowNull) {
        JTextField textFields[] = {jTextField1, jTextField2, jTextField3, jTextField4};
        Properties oldProp = loadPropertiesFile();
        
        int len = textFields.length;
        String oldData[] = new String[len];
        if(oldProp != null)
        {
            for(int i =0;i<len;i++)
            {
                oldData[i] = oldProp.getProperty(propertiesKey[i]);
            }
        }
        
        System.out.println("creating properties file !");
        Properties prop = new Properties();
        for(int i=0;i<data.length;i++)
        {
            if(allowNull || data[i].length() != 0)
                prop.setProperty(propertiesKey[i], data[i]);
            else
                prop.setProperty(propertiesKey[i], oldData[i]);
        }
        try {
            prop.store(new java.io.FileOutputStream("config.properties"), null);
            System.out.println("created properties file successfully!");
            return true;
        } catch (java.io.IOException ex) {
            ex.printStackTrace();
            return false;
        }
    }
    
    public Properties loadPropertiesFile() {
        System.out.println("trying to load properties file!");
        String propFile = "config.properties";
        Properties prop = new Properties();
        try {
            java.io.FileInputStream f = new java.io.FileInputStream(propFile);
            if(f == null)
                return null;
            prop.load(f);
            System.out.println("Loaded properties file successfully!");
            return prop;
        } catch (java.io.IOException ex) {
            ex.printStackTrace();
        }
        return null;
    }
    
